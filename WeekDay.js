let number = Math.floor(Math.random()*10 % 7)+1;
console.log("DAY: "+number+" -> ");

if(number==1){
    console.log("MONDAY");
}else if(number==2){
    console.log("TUESDAY");
}else if(number==3){
    console.log("WEDNESDAY");
}else if(number==4){
    console.log("THURSDAY");
}else if(number==5){
    console.log("FRIDAY");
}else if(number==6){
    console.log("SATURDAY");
}else if(number==7){
    console.log("SUNDAY");
}